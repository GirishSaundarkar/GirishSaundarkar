package jbk.demo.list;

import java.util.HashSet;

public class Hashset {

	public static void main(String[] args) {

		HashSet<String> h_set=new HashSet<String>();
		h_set.add("red");
		h_set.add("green");
		h_set.add("black");
		h_set.add("white");
		h_set.add("pink");
		h_set.add("yellow");
		System.out.println("The Hashset is:"+h_set);
		
	}

}
